package Model.Effects;

/**
 * The type Empty effect.
 */
public class EmptyEffect implements Effect {

    /**
     * Instantiates a new Empty effect.
     */
    public EmptyEffect(){

    }

    /**
     * To string string.
     *
     * @return the string
     */
    public String toString(){
        String str = new String();
        str +=  "Empty Effect\n";

        return str;
    }

}
